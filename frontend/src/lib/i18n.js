// Pick the localized label for an item that has { korean, english } fields.
// Prefers the localized entry from CAT_T (when item has an id), else falls back
// to korean for `ko` locale and english for all others.
export function catLabel(item, lang, type) {
  if (!item) return "";
  if (type && item.id && CAT_T[lang] && CAT_T[lang][type] && CAT_T[lang][type][item.id]) {
    return CAT_T[lang][type][item.id];
  }
  if (lang === "ko") return item.korean || item.english || "";
  return item.english || item.korean || "";
}

